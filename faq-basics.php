                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">What is 9-1-1?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Nine-one-one is the number most people in the U.S. and some in international countries call to get help in a police, fire or medical emergency. In some places, you may also be able to be connected with Poison Control by calling 9-1-1, but you should check with your local officials to make sure. A 9-1-1 call goes over dedicated networks to the appropriate 9-1-1 answering point (PSAP) for the caller's location, and trained personnel then send the emergency help needed.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">What is Enhanced 9-1-1?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Enhanced 9-1-1, or E9-1-1, is a system that routes an emergency call to the appropriate 9-1-1 answering point (PSAP) for the caller's location AND automatically displays the caller's phone number and address. The 9-1-1 call-taker will typically ask the caller to verify the information that appears on his or her computer screen. In most areas, phone number and location information are available for 9-1-1 calls made from a cellular/wireless phone.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Who pays for 9-1-1?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>In most areas, each household or business pays a small monthly fee for 9-1-1 service on each telephone line that appears on their phone bill. There is no per-call charge for calling 9-1-1. However, EMS/ambulances dispatched through 9-1-1 may charge for taking someone to the hospital; this is a separate ambulance charge, not a 9-1-1 charge.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">When should you use 9-1-1?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Nine-one-one is only to be used in emergency situations. An emergency is any situation that requires immediate assistance from the police/sheriff, the fire department, or an ambulance. If you are ever in doubt of whether a situation is an emergency, you should call 9-1-1. It's better to be safe and let the 9-1-1 call-taker determine if you need emergency assistance.</p>

                                    <h5 class="font-size-32">Do not call 9-1-1:</h5>
                                    <ul>
                                        <li>for information</li>
                                        <li>for directory assistance</li>
                                        <li>when you're bored and just want to talk</li>
                                        <li>for paying traffic tickets</li>
                                        <li>for your pet</li>
                                        <li>as a prank</li>
                                    </ul>

                                    <p>If you call 9-1-1 by mistake, do not hang up. Tell the call-taker what happened so they know there really isn't an emergency.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">What about 9-1-1 prank calls?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>It's a prank call when someone calls 9-1-1 for a joke, or calls 9-1-1 and hangs up. Prank calls not only waste time and money, but also can be dangerous. If 9-1-1 lines or call-takers are busy with prank calls, someone with a real emergency may not be able to get the help they need. In most places, it's against the law to make prank 9-1-1 calls.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">How do I make a 9-1-1 call?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>In an emergency, dial 9-1-1 on your phone. It's a free call. You can use any kind of phone: push button, rotary, cellular/wireless, cordless, or pay phone. (With some pay phones, you may need coins to get a dial tone; with many wireless phones, Enhanced 9-1-1 does not yet work.)</p>
                                    <ul>
                                        <li>Stay calm and state your emergency</li>
                                        <li>Speak loudly and clearly. Give the 9-1-1 call-taker your name, phone number and the address where help is needed.</li>
                                        <li>Answer the call-taker's questions. Stay on the telephone if it's safe to do so, and don't hang up until the call-taker   tells you to.</li>
                                    </ul>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">What if a 9-1-1 caller doesn't speak English?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>When necessary, a 9-1-1 call-taker can add an interpreter from an outside service to the line. A non-English speaking caller may hear a short conversation in English and some clicking sounds as the interpreter is added to the line.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">What if a 9-1-1 caller is deaf, or hearing/speech impaired?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Communications centers that answer 9-1-1 calls have special text telephones for responding to 9-1-1 calls from deaf or hearing/speech impaired callers. If a caller uses a TTY/TDD, the caller should:</p>
                                    <ul>
                                        <li>Stay calm, place the phone receiver in the TTY, dial 9-1-1.</li>
                                        <li>After the call is answered, press the TTY keys several times. This may help shorten the time necessary to respond to the call.</li>
                                        <li>Give the call-taker time to connect their TTY. If necessary, press the TTY keys again. The 9-1-1 call-taker should answer and type "GA" for Go Ahead.</li>
                                        <li>Tell what is needed â€“ police, fire department, or ambulance. Give your name, phone number, and the address where help is needed.</li>
                                        <li>Stay on the telephone if it is safe. Answer the call-taker's questions.</li>
                                    </ul>

                                    <p>If a deaf or hearing/speech impaired caller doesn't have a TTY/TDD, the caller should call 9-1-1 and don't hang up. Not hanging up leaves the line open. With most 9-1-1 calls, the caller's address is displayed on the call-taker's screen and help will be sent.</p>
                                    <p><strong>TEXT to 9-1-1 is available to all callers in Hamilton County using an SMS-enabled device (cell phone). </strong></p>
                                </div>
                            </div>
                        </li>
