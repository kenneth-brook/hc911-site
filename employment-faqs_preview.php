                                <h4 class="question cushycms" title="Question">What is the training to become a Telecommunicator?</h4>
                                <div class="answer cushycms" title="Answer"><p>New hires will take part in the Training Academy, which consists of 8 weeks of classroom training, where they will receive the necessary skills and certifications to train on the floor. After the successful completion of the Training Academy, Telecommunicator Trainees will begin on-the-floor training.</p>

<p>Trainees will learn various disciplines on the floor, from call-taking to dispatching to INFO/NCIC. On-the-floor training typically lasts 10 months, and will consist of training with Communication Training Officers, as well as independent time. If needed, additional training time can be provided.</p>
</div>
