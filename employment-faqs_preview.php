                                <h4 class="question cushycms" title="Question">What kind of hours or shifts can I expect to work as a Telecommunicator?</h4>
                                <div class="answer cushycms" title="Answer"><p>We work five, 8-hour shifts totaling 40 hours a week, with rotating off days. As an extremely important, irreplaceable link in public safety, our positions must be staffed 24 hours a day, 7 days a week. Mandatory overtime can be implemented. Good attendance and willingness to work hours other than day shift are vital.</p>

<p>Telecommunicators are subject to work ANY shift (1st, 2nd, or 3rd), holidays, and weekends. Shift assignments are semi-permanent (based upon seniority) and new hires must accept that it may be years before they accumulate enough seniority to obtain a day (1st) shift assignment.</p>
</div>
