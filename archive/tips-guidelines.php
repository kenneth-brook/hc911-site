                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Know the phones you own.</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Educate everyone in your household about the phone system in your home as well as your cell phone. Children may need to use the devices in an emergency and will need to know how to operate them.</p>
                                </div>
