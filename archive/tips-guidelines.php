                                <h4 class="question cushycms" title="Question">Is a person hurt or in danger? Do you need the police, fire, or ambulance?</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Have you ever wondered whether to call 9-1-1? Since 9-1-1 is for emergencies only, it helps to understand when to call and when not to call. An emergency is any serious situation where a law enforcement officer, fire fighter, or emergency medical help is needed right away. If you are unsure of whether your situation is an emergency, go ahead and call 9-1-1. The 9-1-1 call-taker can determine if you need emergency assistance and can route you to the correct location.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">If you do call 9-1-1, even by mistake, do not hang up the phone.</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>9-1-1 call-takers are trained to get the most important information as quickly as possible to get help on the way to an emergency situation. In an emergency, allow the call-taker to ask you all the questions they need to get help to your location in the timeliest manner before you hang up or leave the phone. If you happen to call by accident, stay on the line until you can tell the call-taker that you called by accident and there is no emergency. This saves the call-taker from having to call you back and confirm there is no emergency or sending police with lights and sirens to check your address for an emergency.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">When calling 9-1-1, do your best to stay calm and answer all questions.</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Staying calm can be one of the most difficult, yet most important, things you do when calling 9-1-1. It is very important that you stay as calm as possible and answer all the questions the 9-1-1 call-taker asks. The questions 9-1-1 call-takers ask, no matter how irrelevant they may seem, are important in helping get the first responders to you as fast as possible.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Help the 9-1-1 call-taker help you.</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Listen and answer the questions asked. By doing this, you help the call-taker understand your situation and enable them to assist you with your emergency until the appropriate police, fire or medical units arrive.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Know the location of the emergency.</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>The wireless 9-1-1 caller must be aware that the 9-1-1 center that answers the call may not be the 9-1-1 center that serves the area you are calling from. Look for landmarks, cross street signs, and buildings. Know the name of the city or county you are in. Knowing the location is vital to getting the appropriate police, fire or EMS units to respond. Providing an accurate address is critically important when making a wireless 9-1-1 call.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Teach your children how to call 9-1-1.</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Be sure your child knows what 9-1-1 is, how to dial from your home and cell phone, and to trust the 9-1-1 call-taker. Make sure your child is physically able to reach at least one phone in your home. When calling 9-1-1, your child needs to know their name, parent's name, telephone number, and most importantly, their address. Tell them to answer all the call-taker’s questions and to stay on the phone until instructed to hang up. For more information please see our <a href="9-1-1-for-kids.php">9-1-1 for Children page</a>.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Prank calls to 9-1-1 waste time and are illegal in most states.</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Be sure all members of your household are aware that prank or harassing calls to 9-1-1 will be dealt with by local law enforcement agencies. The penalty for prank calls can be serious.​</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Post your address clearly and prominently at your entrance and on your home.</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Posting your 9-1-1 address at the driveway entrance and on your home will alleviate any confusion as to whether emergency responders have the correct location. Try using something reflective or illuminated so that it can be seen in the evening as well as during the day. DO NOT ASSUME SINCE YOUR MAILBOX IS MARKED YOU HAVE POSTED YOUR ADDRESS – mailboxes are not always at the driveway entrance and often are not marked clearly on both sides. Several cities and counties have ordinances for posting 9-1-1 addresses – check with your local ones. Always report missing street signs when noted – these not only help others find your home, but are essential to emergency response personnel.</p>
                                </div>
                            </div>
                        </li>
