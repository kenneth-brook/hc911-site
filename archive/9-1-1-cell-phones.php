                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Basic 9-1-1</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Basic 9-1-1 means that when the three-digit number is dialed, the call is delivered across dedicated circuits to a call-taker/dispatcher in a local public safety answering point (PSAP), or 9-1-1 center. The emergency and its location are communicated by voice between the caller and the call-taker.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Enhanced 9-1-1 or E9-1-1</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>In areas serviced by E9-1-1, the call is selectively routed and the local 9-1-1 center has equipment and database information that allows the call-taker to see the caller's phone number and address on a display. This lets them quickly dispatch emergency help, even if the caller is unable to communicate where they are or what is the emergency.</p>
                                    <p>However, when 9-1-1 calls are made from wireless phones, the call may not be routed to the most appropriate 9-1-1 center, and the call-taker doesn't receive the callback phone number or the location of the caller. This presents life threatening problems due to lost response time, if callers are unable to speak or don't know where they are, or if they don't know their wireless phone callback number and the call is dropped.</p>
                                    <p>There are 3 stages referred to in implementing Wireless 9-1-1. The most basic of these, sometimes unofficially called Wireless Phase 0, simply means that when you dial 9-1-1 from your cell phone, a call-taker at a public safety answering point (PSAP) answers. The call-taker may be at a state highway patrol PSAP, at a city or county PSAP many miles away, or at a local PSAP, depending on how the wireless 9-1-1 call is routed.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Wireless E9-1-1 Phase I</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>This is the first step in providing better emergency response service to wireless 9-1-1 callers. When Phase I has been implemented, a wireless 9-1-1 call will come into the PSAP with the wireless phone callback number. This is important in the event the cell phone call is dropped, and may even allow PSAP employees to work with the wireless company to identify the wireless subscriber. However, Phase I still doesn't help call-takers locate emergency victims or callers.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Wireless E9-1-1 Phase II</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>To locate wireless 9-1-1 callers, Phase II must be implemented in the area by local 9-1-1 systems and wireless carriers. Phase II allows call-takers to receive both the caller's wireless phone number and their estimated location information.</p>

