                                <div class="answer cushycms" title="Answer">
                                    <p>The District was an early adopter of RapidSOS location technology.  RapidSOS utilizes an advanced location feature built into most Apple and Android devices that allows for much more precise location accuracy than Phase I or Phase II can provide.  This can greatly assist in getting first responders to the caller's location much more quickly, especially when the caller may not be familiar with their surroundings.</p>
