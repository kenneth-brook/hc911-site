                                    <p>In our increasingly wireless society, more and more of the public dials 9-1-1 from a mobile device. CTIA-The Wireless Association reported that about 86 million Americans subscribed to wireless telephone service in 1999. Today, that figure has increased to a whopping 302.9 million wireless subscriber connections. In addition, Americans are increasingly cutting the cord: The Centers for Disease Control and Prevention puts the fraction of wireless-only households in the United States at 29.7%. This naturally leads to a significant increase in wireless 9-1-1 calls. According to CTIA, more than 296,000 wireless calls are made to 9-1-1 every day.</p>
