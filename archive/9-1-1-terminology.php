                                <h4 class="question cushycms" title="Question">Basic 9-1-1</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Basic 9-1-1 means when you dial the three-digit number, a call-taker/dispatcher in the local public safety answering point (PSAP), or 9-1-1 call center, answers the call when the call is made from landline phones, such as a home or business phone. The emergency and its location are communicated by voice (or TTY) between the caller and the call-taker.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Enhanced 9-1-1</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>In areas serviced by Enhanced 9-1-1, the call is selectively routed to the proper PSAP for the caller's location when the call is made from landline phones, such as a home or business phone, and the PSAP has equipment and database information that display the caller's phone number and address to the call-taker. 93% of counties with 9-1-1 coverage have Enhanced 9-1-1 for callers. The term "enhanced 9-1-1" is not synonymous with wireless 9-1-1.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Wireless Phase I</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>80% of an estimated 240 million 9-1-1 calls are made from wireless devices, according to <a href="https://www.ctia.org/the-wireless-industry/infographics-library" target="_blank">CTIA</a>. When Phase I has been implemented, the call-taker automatically receives the wireless phone number. This is important in the event the wireless phone call is dropped, and may allow PSAP employees to work with the wireless company to identify the wireless subscriber. Phase I also delivers the location of the cell tower handling the call. The call is routed to a PSAP based on cell site/sector information.</p>
                                </div>
                            </div>
                        </li>
                        <li>
                            <div class="faq-item">
                                <h4 class="question cushycms" title="Question">Wireless Phase II</h4>
                                <div class="answer cushycms" title="Answer">
                                    <p>Phase II allows call-takers to receive both the caller's wireless phone number and their location information based on GPS coordinates obtained through handset triangulation provided by the telephone company. The call is routed to a PSAP either based on cell site/sector information or on caller location information.</p>
                                </div>
                            </div>
                        </li>
