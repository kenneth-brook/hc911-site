                        <h2 class="font-size-44 color-yellow">Why NG9-1-1 ?</h2>
                        <hr class="mt-0">
                        <h5 class="font-size-40">The purposes of NG9-1-1 can be briefly described in four segments:</h5>
                    </div>

                    <ul class="segment-list cushycms" title="Content middle">
                        <li>
                            <div class="highlight-box cushycms">
                                <h3 class="color-blue-dark font-size-26 font-weight-bold line-height-reset mb-3">1. It fully replaces Enhanced 9-1-1 , with all capabilities and functions in place today.</h3>
                                <p>We cannot drop features of E9-1-1 as we change over to a new base technology (IP) and entirely different, software-based and database control mechanisms. This applies right down to seldom used but critical features for dealing automatically with real time call routing and delivery problems, or troubleshooting of call and data issues. All current originating service types must continue to be supported seamlessly, with no service dropout during the transition from E9-1-1 to NG9-1-1 .</p>
                            </div>
                        </li>
                        <li>
                            <div class="highlight-box cushycms">
                                <h3 class="color-blue-dark font-size-26 font-weight-bold line-height-reset mb-3">2. It adds capabilities to support changes for current and new types of Originating Service Providers.</h3>
                                <p>E9-1-1 supports voice calling for wireline, cellular, and VoIP service providers today. There are current and future needs for different and new calling technologies, including non-voice messaging of various types, devices generating data-only messages (such as sensors), photo and video transmission, and unknown future services. A primary objective is to establish a common, IP-based interface that developers can design to as they develop new services, so that 9-1-1 can be planned for and then connected to quickly as call and message-generating services are introduced to the public.</p>
                            </div>
                        </li>
                        <li>
                            <div class="highlight-box cushycms">
                                <h3 class="color-blue-dark font-size-26 font-weight-bold line-height-reset mb-3">3. It adds flexibility for the PSAPs and 9-1-1 authorities.</h3>
                                <p>This flexibility ranges from the ability to transfer calls, messages, and data between any PSAPs on any interconnected NG9-1-1 system anywhere in the country (and beyond), to the ability to directly activate alternate routing much more quickly, to controlling data flow. The PSAP will be able to access a wide range of supportive databases and share new and more robust forms of data to facilitate call processing, emergency response and comprehensive incident management. Basic tools to support disaster-related 9-1-1 call control and to handle non-voice call types are also involved.</p>
                            </div>
                        </li>
                        <li>
                            <div class="highlight-box cushycms">
                                <h3 class="color-blue-dark font-size-26 font-weight-bold line-height-reset mb-3">4. It adds capabilities to integrate and interoperate with emergency entities beyond the PSAP.</h3>
                                <p>Other emergency and public safety related entities will be able to interconnect to the NG9-1-1 network and system, and be able to receive calls and data sent by the NG9-1-1 system or PSAPs, as well as  acquire and pass data between all entities (with access controls). Inherent in this portion is support for disaster management and intercommunications with and between PSAPs, EOCs, DHS, and other emergency management entities.</p>
                            </div>
                        </li>
                    </ul>
