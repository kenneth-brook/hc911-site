                            <p>After 15 years of co-location and 20 years since the original study, unification was achieved in January 2009 when the District implemented Unified Emergency Communications (UEC) in Hamilton County. Through this agreement, the District contracted with other public safety agencies to assume all call-taking and dispatching operations for most emergency service agencies in Hamilton County with the exception of Soddy-Daisy, Lookout Mountain, and the University of Tennessee-Chattanooga.</p>
                            <p>This monumental task saw call-taking and dispatch services for more than two dozen responder agencies – including law enforcement, emergency medical services, and paid and volunteer fire departments – centralized and brought under the direction of the District. To complete this daily job, 130 telecommunicators answer 9-1-1 calls and send responders to 366,000 residents and countless thousands of visitors within Hamilton County's 542 square miles. These frontline telecommunicators are supported by 30 supervisors and administrators.</p>

