                        <div class="col cushycms" title="Content left">
                            <p><strong class="font-size-26 line-height-12 color-blue-dark">The Hamilton County Emergency Communications District was established in November 1984,</strong> when 89% of voters passed a local referendum establishing 9-1-1 service. Following referendum approval, the County Commission created the Emergency Communications District in accordance with State law (T.C.A. 7-86-101), approving Resolution <span class="d-inline-block">1284-17</span>. </p>
                            <p>The resolution called for appointment of nine (9) members of the Board of Directors, serving as an independent governing body for the District. Pursuant to statutory changes, the Board was increased to eleven (11) members in 2001. All funding for the Hamilton County 9-1-1 District operations come from surcharges assessed to telephone bills.</p>

                            <img src="images/contents/hc9-1-1-history-1.jpg" alt="" class="full-width">

                            <p>In 1989, the Board authorized a feasibility study on unifying the eight (8) separate "public safety answering points" (PSAPs) operating in the county at that time. Several years later, the Board moved the 9-1-1 Center to its current location on Amnicola Highway in Chattanooga to co-locate PSAP operations. Hamilton County EMS, Sheriff's Office, East Ridge, and Chattanooga accepted the invitation and relocated to the Center. </p>
                            <p>The cities of Collegedale and Red Bank were then contracting with the Sheriff's Office for communications services, and those municipalities were also affected by the co-location. Although co-location improved coordination among the major PSAPs, it did not resolve issues of separate management systems and limited efficiencies. </p>
