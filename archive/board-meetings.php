                    <div class="inner inner-top text-center cushycms" title="Content top"><p><strong class="font-size-32 font-weight-semibold line-height-11">Board Meetings</strong><br />
&nbsp;</p>

<p>Regular meetings of the Hamilton County Emergency Communications District <a href="https://www.hc911.org/board-members.php">Board of Directors</a> are held bi-monthly beginning in January of each year. There is an additional board meeting held in June.</p>

<p>All regular Board meetings are held on the 2nd Thursday of the month at 9:00 AM at the District&#39;s offices at 3404 Amnicola Hwy and are open to the public.&nbsp; Persons attending the meeting are subject to search before being admitted to the building, and photo ID will be required.</p>
&nbsp;

<p><strong class="font-size-32 font-weight-semibold line-height-11">Public Comment</strong></p>
&nbsp;

<p>The Board shall reserve a period for public comment at each meeting for comments relevant to a subject on the agenda, as long as prior notice has been given. Those wishing to speak in front of the Board must give notice no less than three (3) working days prior to the day of the Board meeting.&nbsp; This can be done by calling 423-622-1911 or emailing <a href="mailto:meetingrequest@hc911.org" style="color:#0563c1; text-decoration:underline">meetingrequest@hc911.org</a>.&nbsp;</p>
&nbsp;

<p>The Board will entertain up to five (5) speakers at one meeting; the Chairman may allow more speakers to comment should the situation warrant.&nbsp; Each speaker will be given five (5) minutes of time for comments.</p>
